public class A {
    {
        // 2-й шаг: первый нестатический блок выполняется при создании объекта
        // В этот момент поле id еще не инициализировано явно, поэтому имеет значение по умолчанию 0
        System.out.println("logic (1) id= " + this.id);
    }

    static {
        // 1-й шаг: статический блок выполняется ПЕРВЫМ при загрузке класса A в память
        // Выполняется только один раз, даже если создавать несколько объектов
        System.out.println("static logic");
    }

    private int id = 1; // 3-й шаг: инициализация поля id значением 1

    public A(int id) {
        // 5-й шаг: конструктор выполняется ПОСЛЕ всех нестатических блоков
        this.id = id; // Присваиваем полю значение из параметра (100)
        System.out.println("ctor id= " + id); // Выводим значение параметра
    }

    {
        // 4-й шаг: второй нестатический блок выполняется после инициализации поля id
        // В этот момент id уже равно 1 (из строки private int id = 1)
        System.out.println("logic (2) id= " + id);
    }
}

public class Main {
    public static void main(String[] args) {
        new A(100); // Создание объекта A с параметром 100
    }
}

/* Вывод:
static logic
logic (1) id= 0
logic (2) id= 1
ctor id= 100*/