class A {
    static void method() {
    }
}

class B extends A {
    @Override
    static void method() {
    }
}

public class StaticMethod {
    public static void main(String[] args) {
        A a = new A();
        B b = new B();
        A ab = new B();  // Полиморфная ссылка
        
        a.method();    // Статический метод класса A
        b.method();    // Статический метод класса B
        ab.method();   // Статический метод класса A
        
        // Динамический полиморфизм не работает со статическими методами,
        // потому что статические методы связаны с классом, а не с объектом.
        // Вызов статического метода определяется на этапе компиляции
        // по типу переменной (ссылке), а не по типу фактического объекта.
        // При вызове ab.method() используется метод класса A, а не B,
        // хотя ab ссылается на объект класса B.
    }
}